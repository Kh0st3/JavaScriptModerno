// Inicializar una variables con un valor
var producto = "Monitor de 27 pulgadas";
console.log(producto);

// Las variables se pueden reasignar
producto = "Monitor de 24 pulgadas";
console.log(producto);

// JavaScript es un lenguaje de tipo dinamico, no se especifica tipo de dato
producto = 20;
console.log(producto);

// Se pueden inicializr sin valor y asignarlo despues
var disponible;
disponible = true;

disponible = false;


